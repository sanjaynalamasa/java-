public class switch_stmnt {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1 : System.out.println("light ONN");
                break;
            case 2 : System.out.println("Fan ONN"); 
                break;
            case 3 : System.out.println("Charging port ONN");
                break;
            default : System.out.println("enter valid number");
        }
    }
    
}
