public class Assignmentoperator {
    public static void main(String args[]) {
        int a = 10;
        //a = a + 10;
        a += 5; //faster execution 
        System.out.println(a);
        // a = a * 5;
        a *=10;
        System.out.println((a));
        // a = a /  5; 
        a /=15;
        System.out.println(a);
        // a = a % 10
        a %= 10;
        System.out.println(a);
    }
    
}
